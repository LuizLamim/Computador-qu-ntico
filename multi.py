def multiplicar_numeros(a, b):
  """
  Multiplica dois números e retorna o resultado.
  """
  resultado = a * b
  return resultado

# Exemplo de uso:
numero1 = 15
numero2 = 7

produto = multiplicar_numeros(numero1, numero2)

# Imprime o resultado
print(f"O resultado da multiplicação de {numero1} por {numero2} é: {produto}")