import numpy as np # Importa a biblioteca NumPy e a apelida de 'np'

# Criação dos vetores unitários usando np.array
# Vetor unitário na direção X
vetor1 = np.array([1.0, 0.0, 0.0]) 
# Vetor unitário na direção Y
vetor2 = np.array([0.0, 1.0, 0.0])

# Exibe os vetores
print("Primeiro vetor unitário:", vetor1)
print("Segundo vetor unitário:", vetor2)