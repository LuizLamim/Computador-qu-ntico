import math


def funcao_exponencial(x):
    return 5 ** x


# Exemplo de uso
expoente = 2
resultado = funcao_exponencial(expoente)
print(f"5^{expoente} = {resultado}")

expoente = 3.5
resultado = funcao_exponencial(expoente)
print(f"5^{expoente} = {resultado}")

expoente = -1
resultado = funcao_exponencial(expoente)
print(f"5^{expoente} = {resultado}")
