# Pede ao usuário para digitar um número
# A função 'input()' sempre retorna uma string, então usamos 'float()' para converter para número
numero = float(input("Digite um número para ser elevado ao quadrado: "))

# Calcula o quadrado do número usando o operador '**'
# O operador '** 2' significa "elevado à potência de 2"
quadrado = numero ** 2