function somarNumeros(numero1, numero2) {
  return numero1 + numero2;
}

const a = 5;
const b = 10;

const resultado = somarNumeros(a, b);

console.log("O primeiro número é:", a);
console.log("O segundo número é:", b);
console.log("A soma dos dois números é:", resultado);

const x = 25.5;
const y = 14.5;
const resultado2 = somarNumeros(x, y);

console.log("\n--- Outro Exemplo ---");
console.log("A soma de", x, "e", y, "é:", resultado2);